// Which of the following are examples of node modules?

// a. Express

// b. Body-parser

// c. Socket.io

// d. All of the above

//Answer: d. All of the above

//EXPLANATION:

// a. Express:
//    Express is a web framework for Node.js, which provides a simple and flexible set of features to develop
//    web and mobile applications.It simplifies the process of building web servers and handling HTTP requests
//    and responses.

// b. Body-parser:
//    Body-parser is a middleware for Express.js, used to parse incoming request bodies in a middleware before
//    reaching the route handlers. It extracts the entire body portion of an incoming request stream and exposes
//    it on req.body. This module is commonly used for parsing JSON, URL-encoded, and multipart form data.

// c. Socket.io:
//    Socket.io is a JavaScript library for real-time web applications. It enables real-time, bidirectional, and
//    event-based communication between web clients and servers. Socket.io works on every platform, browser, or
//    device, focusing equally on reliability and speed.

// All of these modules are commonly used in Node.js applications, each serving different purposes in web
// development. Express provides a framework for building web applications, Body-parser helps in parsing
// request bodies, and Socket.io facilitates real-time communication between clients and servers.
